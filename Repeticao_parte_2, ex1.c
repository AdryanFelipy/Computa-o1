#include <stdio.h>

int main()
{
    int num, maior;

    printf ("Digite numeros (negativos para sair): \n");
        scanf ("%d", &num);
    maior = num;

    while (num >= 0)
    {
        if (num > maior)
        {
            maior = num;
        }

        printf("Digite um numero (negativo para sair): \n");
        scanf ("%d", &num);

    }
    printf("O maior numero digitado foi: %d\n", maior);

    return 0;
}
